    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int index = tbGhe.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm trước khi xóa đê :)) ");
                return;
            }
            int index1 = tbGhe.getRowCount();
            if (index1 == 0) {
                JOptionPane.showMessageDialog(this, "Không có dữ kiệu có dữ liệu để xóa :))");
                return;
            }
            int check = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa khum ? ");
            if (check != JOptionPane.YES_OPTION) {
                return;
            } else {
                String idString = tbGhe.getValueAt(index, 0).toString();
                Integer id = Integer.parseInt(idString);
                if (gheService.xoa(id) == true) {
                    JOptionPane.showMessageDialog(this, "CHắc là xóa thành công rồi :)) ");

                } else {
                    JOptionPane.showMessageDialog(this, " Chắc là Xóa thất bại rồi :))");
                }
                hienthi();
            }
        } catch (Exception e) {
        }


    }//GEN-LAST:event_btnXoaActionPerformed

