    public void timTheoDanhMuc() {
        try {
            String danhMuc = (String) cbbTimTheoTheLoai.getSelectedItem();
            Integer id = null;
            List<Category> categorys = categoryService.layAllCategory();
            for (Category category : categorys) {
                if (danhMuc.equals(category.getCategoryName())) {
                    id = category.getId();
                }
            }
            DefaultTableModel model = (DefaultTableModel) tbGhe.getModel();
            model.setRowCount(0);

            List<Ghe> ghes = gheService.searchbyCategory(id);

            for (Ghe ghe : ghes) {
                Object[] data = new Object[]{
                    ghe.getId(),
                    danhMuc,
                    ghe.getTen(),
                    ghe.getGia(),
                    ghe.getSoLuong(),
                    ghe.getKichThuoc(),
                    ghe.getXuatSu()
                };
                model.addRow(data);
            }
            JOptionPane.showMessageDialog(this, "Chắc là tìm kiếm thành công rồi :))");
        } catch (SQLException ex) {
            Logger.getLogger(QuanLyForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

