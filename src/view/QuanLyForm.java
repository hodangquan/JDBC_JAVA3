    public void hienthi() {
        DefaultTableModel model = (DefaultTableModel) tbGhe.getModel();
        model.setRowCount(0);
        List<Ghe> ghes = gheService.layAllXeDaps();
        List<Category> categorys = categoryService.layAllCategory();
        String tenDanhMuc = "";
        for (Ghe ghe : ghes) {
            for (Category category : categorys) {
                if (category.getId() == ghe.getCategoryId()) {
                    tenDanhMuc = category.getCategoryName();
                }
            }
            if (ghe.isKhaDung() == true) {
                Object[] data = new Object[]{
                    ghe.getId(),
                    tenDanhMuc,
                    ghe.getTen(),
                    ghe.getGia(),
                    ghe.getSoLuong(),
                    ghe.getKichThuoc(),
                    ghe.getXuatSu()
                };
                model.addRow(data);
            }
        }
    }

